#!/bin/bash
mvn clean package

JAVA_OPTS="-Dfile.encoding=UTF-8"
JAVA_OPTS="$JAVA_OPTS -Duser.timezone=GMT-2 "
echo "---------------------------------------------------------------------------"
echo "Java OPTS: $JAVA_OPTS"
echo "---------------------------------------------------------------------------"
echo " "

echo "---------------------------------------------------------------------------"
echo "running (after)"
echo "---------------------------------------------------------------------------"
java -jar target/java-ops-bcp-1.0.jar $JAVA_OPTS

echo "---------------------------------------------------------------------------"
echo "running (before)"
echo "---------------------------------------------------------------------------"
java -jar $JAVA_OPTS target/java-ops-bcp-1.0.jar

echo "---------------------------------------------------------------------------"
echo "running (before as String)"
echo "---------------------------------------------------------------------------"
java -jar "$JAVA_OPTS" target/java-ops-bcp-1.0.jar